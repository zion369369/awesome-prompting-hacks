import { execSync } from 'child_process';
import fs from 'fs';
import path from 'path';

function getChangelog() {
  console.log('[START] Generating release changelog...');
  try {
    const databasePath = 'src/data/scraped_prompts.json';
    
    // 1. Get current prompts
    if (!fs.existsSync(databasePath)) {
      throw new Error(`Database file not found: ${databasePath}`);
    }
    const currentData = JSON.parse(fs.readFileSync(databasePath, 'utf-8'));
    
    // 2. Get previous prompts from HEAD~1
    let previousData = [];
    try {
      const prevRaw = execSync(`git show HEAD~1:${databasePath}`, { encoding: 'utf-8', stdio: ['pipe', 'pipe', 'ignore'] });
      previousData = JSON.parse(prevRaw);
    } catch (e) {
      console.warn('[WARN] Could not retrieve previous commit database. Defaulting to empty list.');
    }

    // 3. Find newly added prompts
    const prevSlugs = new Set(previousData.map(p => p.slug));
    const addedPrompts = currentData.filter(p => !prevSlugs.has(p.slug));

    // 4. Build changelog markdown
    let body = `## 🚀 What's New in this Release\n\n`;
    if (addedPrompts.length === 0) {
      body += `This release contains library updates, optimizations, schema fixes, or structural upgrades.\n`;
    } else {
      body += `We added **${addedPrompts.length}** new production-grade prompt templates to the catalog:\n\n`;
      addedPrompts.forEach(p => {
        body += `- **${p.title}** (Category: \`${p.category}\`)\n  *View interactive template at: https://zion369369.github.io/awesome-prompting-hacks/prompts/${p.slug}*\n`;
      });
    }

    body += `\n\n---\n*Auto-generated by the Awesome Prompting Hacks Release Automation Pipeline.*`;
    
    fs.writeFileSync('RELEASE_CHANGELOG.md', body, 'utf-8');
    console.log('[SUCCESS] Changelog written to RELEASE_CHANGELOG.md');
  } catch (error) {
    console.error('[ERROR] Failed to generate changelog:', error.message);
    process.exit(1);
  }
}

getChangelog();
